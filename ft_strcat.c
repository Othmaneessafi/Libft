#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		j;
	int		i;
	char	*src;

	i = 0;
	j = 0;
	src = (char *)s2;
	while (s1[j] != '\0')
		j++;
	if (s1[j] == '\0')
	{
		while (src[i] != '\0')
		{
			s1[j] = src[i];
			i++;
			j++;
		}
	}
	s1[j] = '\0';
	return (s1);
}
