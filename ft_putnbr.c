#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(214748364);
		ft_putchar(8 + '0');
	}
	else
	{
		if (n < 0 && n != -2147483648)
		{
			ft_putchar('-');
			n = n * -1;
		}
		if (n <= 9)
		{
			ft_putchar(n + '0');
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putchar(n % 10 + '0');
		}
	}
}
