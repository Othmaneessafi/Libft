#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*s2;

	i = 0;
	s2 = (char *)src;
	while (s2[i])
	{
		dst[i] = s2[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
